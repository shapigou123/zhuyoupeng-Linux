_start:
       // 第一步： 把0x11111111写入0xE0200240（GPJ0CON）位置
	   ldr r0, =0x11111111        //伪指令
	   ldr r1, =0xE0200240        //一般写代码都用伪指令，可以自动判断合法非法并处理！
	   str r0, [r1]               //寄存机间接寻址，把r0中的数写入r1中的数为地址的内存中去！
	   
	   // 第二步： 把0x0写入0xE0200244(GPJ0DAT)位置
	   ldr r0, =0x00
	   ldr r1, =0xE0200244
	   str r0, [r1]
	   
	   ldr r0, =0x0010       //伪指令
	   ldr r1, =0xE02000A0        //一般写代码都用伪指令，可以自动判断合法非法并处理！
	   str r0, [r1]  

       ldr r0, =0x2
